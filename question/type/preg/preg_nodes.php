            $result = qtype_preg_unicode::is_in_range($charlower, $ranges) || qtype_preg_unicode::is_in_range($charupper, $ranges);
        } else {
            $result = qtype_preg_unicode::is_in_range($char, $ranges);
        }
        return ($result xor $this->negative);
    }

    public function tohr() {
        $result = '';
        switch ($this->type) {
            case self::CIRCUMFLEX:
                $result = '^';
                break;
            case self::DOLLAR:
                $result = '$';
                break;
            case self::SET:
                $result = $this->data;
                break;
            case self::FLAG:
                $result = $this->data;
                break;
            case self::UPROP:
                $result = 'todo';
                break;
            case self::META_DOT:
                $result = '.';
            default:
                return '';
        }
        if ($this->negative) {
            $result = '!' . $result;
        }
        return $result;
    }
}

